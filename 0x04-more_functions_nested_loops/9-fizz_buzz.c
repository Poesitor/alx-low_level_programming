#include <stdio.h>

/**
 * main - prints the numbers 1 tp 100. 
 * for multiples of therr, prints Fizz instead
 * for multiples of five prints Buzz instead. 
 * for numbers which are multiples of both three and five print FizzBuzz
 *
 * Return: 0
 */
int main(void)
{
	int i;

	for (i = 1; i <= 100; i++)
	{
		if (i % 3 == 0 && i % 5 == 0)
		{
			printf("FizzBuzz ");
		}
		else if (i % 3 == 0)
		{
			printf("Fizz ");
		}
		else if (i % 5 == 0)
		{
			printf("Buzz ");
		}
		else
		{
			printf("%d ", i);
		}
	}
	printf("\n");
	return (0);
}
